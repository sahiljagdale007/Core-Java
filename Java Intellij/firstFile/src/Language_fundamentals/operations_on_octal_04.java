package Language_fundamentals;

public class operations_on_octal_04 {
    public static void main(String[] args) {
        int a = 0123;
        int b = 012;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
