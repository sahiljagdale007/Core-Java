package Language_fundamentals;

public class operations_of_hexadecimal_05 {
    public static void main(String[] args) {
        int a = 0xab;
        int b = 0X012;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
