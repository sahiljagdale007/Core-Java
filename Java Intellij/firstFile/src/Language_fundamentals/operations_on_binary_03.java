package Language_fundamentals;

public class operations_on_binary_03 {
    public static void main(String[] args) {
        int a = 0b1010;
        int b = 0b1010;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
